public class Island {
    private int pointX;
    private int pointy;

    public Island(int pointX, int pointy) {
        this.pointX = pointX;
        this.pointy = pointy;
    }

    public int getPointX() {
        return pointX;
    }

    public int getPointy() {
        return pointy;
    }
}
